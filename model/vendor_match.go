// ResolveDefaultVendor returns the vendor display name + icon for a model name,
// replicating the sync VENDOR_MATCHERS scoring: strongPatterns win, then a
// prefix match, then the longest pattern. Empty strings when nothing matches.
func ResolveDefaultVendor(modelName string) (vendor string, icon string) {
	n := strings.ToLower(modelName)
	bestScore := -1
	bestVendor := ""
	for i := range vendorPatterns {
		p := vendorPatterns[i].pattern
		if !strings.Contains(n, p) {
			continue
		}
		score := len(p)
		if vendorPatterns[i].strong {
			score += 10000
		}
		if strings.HasPrefix(n, p) {
			score += 1000
		}
		if score > bestScore {
			bestScore = score
			bestVendor = vendorPatterns[i].vendor
		}
	}
	if bestVendor == "" {
		return "", ""
	}
	return bestVendor, vendorDisplayIcons[bestVendor]
}
